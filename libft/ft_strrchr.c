char	*ft_strrchr(const char *s, int c)
{
	int	l;

	l = 0;
	if (!s)
		return (0);
	while (s[l])
		l++;
	while (l > 0 && s[l] != c)
		l--;
	if (s[l] == c)
		return ((char *)(s + l));
	return (0);
}
