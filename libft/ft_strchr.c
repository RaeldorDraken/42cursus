char	*ft_strchr(const char *s, int c)
{
	int	l;

	l = 0;
	if (!s)
		return (0);
	while (s[l] && s[l] != c)
		l++;
	if (s[l] == c)
		return ((char *)(s + l));
	return (0);
}
