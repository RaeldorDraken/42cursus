#include "libft.h"

int	ft_isalnum(int c)
{
	int	r1;
	int	r2;

	r1 = 0;
	r2 = 0;
	r1 = ft_isalpha(c);
	r2 = ft_isdigit(c);
	return (r1 + r2);
}
