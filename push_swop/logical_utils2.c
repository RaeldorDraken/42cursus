#include "push.h"

int	exist_less_than(t_stack **s, int nbr)
{
	int	i;

	i = 0;
	while (i < s[0]->size)
	{
		if (s[0]->values[i] <= nbr)
			return (1);
		i++;
	}
	return (0);
}
